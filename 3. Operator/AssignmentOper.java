public class AssignmentOper {
    public static void main(String[] args) {
        int A = 10;
        int B = 12;
        // A = A+10;
        A+= B;
        System.out.println(A);
        // B =B-5;
        B-=5;
        System.out.println(B);
        A*=5;
        System.out.println(A);
        // B/=2;
        // System.out.println(B);
        A/=2;
    
        B%=4;
        System.out.println(A);
        System.out.println(B);
        
    }
}
