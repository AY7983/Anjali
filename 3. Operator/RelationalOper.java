public class RelationalOper {
    public static void main(String[] args) {
        int A = 10;
        int B = 5;
        System.out.println((A == B));
        System.out.println((A!=B));
        System.out.println((A>B));
        System.out.println((A<B));
        System.out.println((A>=B));
        System.out.println((A<=B));
         B = 10;
         System.out.println((A == B));
         System.out.println((A!=B));
         System.out.println((A>B));
         System.out.println((A<B));
         System.out.println((A>=B));
         System.out.println((A<=B));
    }
}
