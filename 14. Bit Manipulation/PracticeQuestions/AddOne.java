public class AddOne {
    public static void main(String[] args) {
        int x = 3;
        System.out.println(x + " + " + 1 + " = " + -~x);

         x = -2;
        System.out.println(x + " + " + 1 + " = " + -~x);

         x = 5;
        System.out.println(x + " + " + 1 + " = " + -~x);
    }
}
