public class DataType {
    public static void main(String[] args) {
        byte b = 8;
        System.out.println(b);
        char ch = 'a'; //it defines only single character
        System.out.println(ch);
        boolean var = true; //boolean data type define only two values true or false
        System.out.println(var);
        float price = 10.25f;
        System.out.println(price);
        int number = 25;
        // long (for int long no.)
        // double(for float but long no.)
        short n=240;
        System.out.println(n);


    }
}
